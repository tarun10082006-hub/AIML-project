function getLang() {
  return localStorage.getItem('pg_lang') || 'en';
}

function setLang(lang) {
  localStorage.setItem('pg_lang', lang);
  applyLang(lang);
  updateSwitcher(lang);
}

function t(key) {
  const lang = getLang();
  return (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) || TRANSLATIONS['en'][key] || key;
}

function applyLang(lang) {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) || TRANSLATIONS['en'][key];
    if (val === undefined) return;
    if (el.children.length === 0) {
      el.innerHTML = val;
    } else {
      const span = el.querySelector('[data-i18n-inner]');
      if (span) span.innerHTML = val;
      else el.innerHTML = val;
    }
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    const val = (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) || TRANSLATIONS['en'][key];
    if (val !== undefined) el.placeholder = val;
  });
  if (typeof onLangChange === 'function') onLangChange(lang);
}

function updateSwitcher(lang) {
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

function initLangSwitcher() {
  const lang = getLang();
  applyLang(lang);
  updateSwitcher(lang);
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      setLang(btn.dataset.lang);
      if (typeof onLangChange === 'function') onLangChange(btn.dataset.lang);
    });
  });
}
